	public void onException(Throwable throwable, WebDriver driver) {
		if (throwable instanceof NoSuchElementException) {
			System.out.println("NoSuchElementException. Taking screenshot");
			File f = ((TakesScreenshot)DriverManager.getDriver()).getScreenshotAs(OutputType.FILE);		
			try {
				DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyyMMdd_HHmmss");  
				LocalDateTime now = LocalDateTime.now();  
				String dateTimeStamp = dtf.format(now); 
				String screenshotLocation = "c:\\temp\\" + dateTimeStamp + ".png";
				FileHandler.copy(f, new File(screenshotLocation));
				System.out.println("Screenshot can be found here: " + screenshotLocation);
			} catch (IOException e) {
				// TODO Auto-generated catch block
				e.printStackTrace();
			}
		}
		
	}

